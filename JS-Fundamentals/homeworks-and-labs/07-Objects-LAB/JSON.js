// JavaScript Object Notation
// open-standard file format that uses text to transmit data objects
// language-independent
// self-describing
// exchange data between browser and server
// lightweight format compared to XML(legacy use)
// human readable

/* SYNTAX
1. Brackets define a JSON
2. KEYS are in double quotes (NO SINGLE QUOTES!)
    -   Property name always has "", no opportunity without like JS
    -   Strings use ""
    -   Numbers don't
    -   Boolean are usable
    -   Can have nested OBJECTS
    -   We can have arrays
    -   NO trailing commas (at the end, unlike JS)
3. KEYS and values are separated by :

{
    "name":"Ivan",
    "age": 25,
}

4. JS --> JSON via JS (transfer JS to JSON via JS)
    -   JSON.stringify(JSobject here) - СТРИНГОСАЙ

    -   JSON.parse(put JSON string here) - ДЕСТРИНГОСВАШ


*/
